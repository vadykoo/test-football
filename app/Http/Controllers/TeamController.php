     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name'=>'required'
        ]);

        $contact = new Team([
            'name' => $request->get('name'),
        ]);
        $contact->save();
        return redirect('/teams')->with('success', 'Contact saved!');
    }

